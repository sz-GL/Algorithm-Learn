package com.itheima.variable;

public class variable4 {
    public static void main(String[] args) {
//        1.定义一个byte类型变量
        byte a=10;
//        2.定义一个short类型变量
        short b=20;
//        3.定义一个int类型变量
        int c=30;
//        4.定义一个long类型变量，long类型变量必须后面加L或者l
        long d=40L;
//        5.定义一个float类型变量，浮点类型变量必须后面加f或者F
        float e=3.14f;
//        6.定义一个double类型变量
        double f=3.14;
//        7.定义一个char类型变量,char类型变量必须使用单引号括起来,且只能保存一个字符
        char g='A';
//        8.定义一个boolean类型变量
        boolean h=true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);


    }
}
