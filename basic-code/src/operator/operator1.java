package operator;

public class operator1 {
    public static void main(String[] args) {
//        1.整数计算
        int a=10;
        int b=20;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println("-----------------------------");

//        2.小数计算
        double c=10.0;
        double d=3.0;

        System.out.println(c+d);
        System.out.println(c-d);
        System.out.println(c*d);
        System.out.println(c/d);
        System.out.println(c%d);
    }
}
