package operator;

public class operator7 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        a += b; // a = a + b
        a -= b;// a = a - b
        a *= b;// a = a * b
        a /= b;// a = a / b
        a %= b;// a = a % b
        System.out.println(a);
//        打印10对20取余
        System.out.println(10%20);

    }
}
