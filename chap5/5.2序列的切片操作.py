s='helloworld'
#切片操作
s1=s[0:5:2]#从0开始到5结束，包括0不包括5，每两位取一个数
print(s1)
#省略开始位置，默认从0开始
s2=s[:5:1]
print(s2)
#省略开始位置，省略步长
s3=s[:5:]
print(s3)
#省略结束位置，默认到序列最后元素，包含最后
s4=s[0::1]
print(s4)
print(s[5::])
print(s[5:])#14行与15行代码功能相同，省略了结束与步长
#还可以省略开始结束位置，只写步长
print(s[::2])

#步长为负数
print(s[::-1])#将字符串变得逆序
print(s[-1:-11:-1])#与上面的指令一致，不包括-11，然后取-1到-10的数